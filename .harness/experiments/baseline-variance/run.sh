#!/usr/bin/env bash
# Baseline variance measurement script (INC-2, AC-A.3 + AC-A.4)
#
# Responsibilities:
#   - Drive N repetitions per input fixture; append per-run metric rows to results.jsonl
#   - Append-only semantics: existing lines are never mutated
#   - Emit/refresh summary.md with mean / σ / 95% CI / sample count and rabbit-hole #6 caveat
#
# Environment switches (see README.md):
#   HARNESS_BASELINE_MODE   = mock (default) | real       (real deferred to INC-8)
#   HARNESS_BASELINE_DRY    = 1 -> append a single deterministic dry record and exit 0
#   HARNESS_BASELINE_REPS   = int, default 5
#   HARNESS_PLN_PROVIDER    = claude (default) | codex   (iter-5+ probe surface)
#   HARNESS_BASELINE_EXTRA_INPUT = path to a single extra input (adversarial probe surface)
#
# Scope note (rabbit-hole #6): this script measures within-provider variance only.
# It does NOT detect correlated failure across frontier models; see summary.md caveat.
set -euo pipefail

HERE="$(cd "$(dirname "$0")" && pwd)"
INPUTS_DIR="$HERE/inputs"
RESULTS="$HERE/results.jsonl"
SUMMARY="$HERE/summary.md"

MODE="${HARNESS_BASELINE_MODE:-mock}"
PROVIDER="${HARNESS_PLN_PROVIDER:-claude}"
REPETITIONS="${HARNESS_BASELINE_REPS:-5}"
DRY="${HARNESS_BASELINE_DRY:-0}"
EXTRA_INPUT="${HARNESS_BASELINE_EXTRA_INPUT:-}"

mkdir -p "$HERE"
[[ -f "$RESULTS" ]] || : > "$RESULTS"

# ---------------------------------------------------------------------------
# helpers
# ---------------------------------------------------------------------------

# Emit a single result row. Fields required by AC-A.3 harness:
#   schema_version (int>=1), stance_agreement_rate (0..1), round3_survival_rate (0..1)
# Optional: input, iteration, provider, mode, ts, error, skipped, note
emit_row() {
  python3 - "$@" <<'PY' >> "$RESULTS"
import json, sys
keys = sys.argv[1::2]
vals = sys.argv[2::2]
row = {}
for k, v in zip(keys, vals):
    # numeric coercion for known numeric keys
    if k in ("schema_version", "iteration"):
        row[k] = int(v)
    elif k in ("stance_agreement_rate", "round3_survival_rate"):
        row[k] = float(v)
    else:
        row[k] = v
sys.stdout.write(json.dumps(row, sort_keys=True) + "\n")
PY
}

# Deterministic pseudo-metric in [0,1] from (input, iteration, provider).
mock_metric() {
  python3 - "$1" "$2" "$3" "$4" <<'PY'
import hashlib, sys
salt = sys.argv[1]
seed = (sys.argv[2] + "|" + sys.argv[3] + "|" + sys.argv[4]).encode()
h = hashlib.sha256(salt.encode() + b"::" + seed).hexdigest()
# 8 hex chars -> int -> normalize to [0,1]; keep 4 decimals to avoid long floats.
print(round(int(h[:8], 16) / 0xFFFFFFFF, 4))
PY
}

# Classify an input path: "text" for UTF-8 text fixtures, "binary" otherwise.
classify_input() {
  local f="$1"
  [[ -f "$f" ]] || { echo missing; return 0; }
  if LC_ALL=C grep -qI '' "$f" 2>/dev/null; then
    echo text
  else
    echo binary
  fi
}

timestamp() {
  date -u +%Y-%m-%dT%H:%M:%SZ 2>/dev/null || date -u
}

# ---------------------------------------------------------------------------
# summarize subcommand
# ---------------------------------------------------------------------------
summarize() {
  python3 - "$RESULTS" "$SUMMARY" <<'PY'
import json, math, sys, os, collections, datetime

res_path, out_path = sys.argv[1], sys.argv[2]
rows = []
if os.path.exists(res_path):
    with open(res_path) as fh:
        for line in fh:
            line = line.strip()
            if not line:
                continue
            try:
                rows.append(json.loads(line))
            except Exception:
                continue

# Group measurement rows (must have both metric fields) by (input, provider, mode)
groups = collections.OrderedDict()
for o in rows:
    if "stance_agreement_rate" not in o or "round3_survival_rate" not in o:
        continue
    if o.get("error") or o.get("skipped"):
        continue
    key = (o.get("input", "(unknown)"), o.get("provider", "-"), o.get("mode", "-"))
    groups.setdefault(key, []).append(o)

def stats(xs):
    n = len(xs)
    if n == 0:
        return None
    mean = sum(xs) / n
    if n >= 2:
        var = sum((x - mean) ** 2 for x in xs) / (n - 1)
        sd = math.sqrt(var)
        # 95% CI half-width using 1.96 * SE (normal approx; noted as approx in template)
        half = 1.96 * sd / math.sqrt(n)
    else:
        sd = 0.0
        half = 0.0
    # clamp printable range defensively
    mean = max(0.0, min(1.0, mean))
    sd = max(0.0, sd)
    return n, mean, sd, mean - half, mean + half

lines = []
lines.append("# Baseline variance summary")
lines.append("")
lines.append("> Auto-generated by `run.sh --summarize`. Do not edit by hand.")
lines.append(f"> Generated at: {datetime.datetime.utcnow().strftime('%Y-%m-%dT%H:%M:%SZ')}")
lines.append("")
lines.append("## Rabbit-hole #6 caveat (correlated provider failure)")
lines.append("")
lines.append("This baseline measures **within-provider variance only**. It does NOT")
lines.append("detect correlated failure across frontier models that share training-data")
lines.append("bias on specific topics (AI safety, recent frameworks). Provider diversity")
lines.append("offers zero protection against this echo-chamber class; cross-provider")
lines.append("ablation (iter-5+) is required. This summary therefore tracks an")
lines.append("echo-chamber floor, not a ceiling — baseline variance alone cannot")
lines.append("prevent correlated provider failure.")
lines.append("")
lines.append("## Metrics (per input × provider × mode)")
lines.append("")
lines.append("Columns: sample count (n), mean, σ (sample stddev), 95% CI (normal approx, 1.96·SE).")
lines.append("")
lines.append("| Input | Provider | Mode | n | stance mean | stance σ | stance 95% CI | survival mean | survival σ | survival 95% CI |")
lines.append("|-------|----------|------|---|-------------|----------|---------------|------------------|---------------|--------------------|")

if not groups:
    lines.append("| _no measurement rows yet_ | - | - | n=0 | - | - | - | - | - | - |")
else:
    for (inp, prov, mode), items in groups.items():
        sa = [float(o["stance_agreement_rate"]) for o in items]
        r3 = [float(o["round3_survival_rate"]) for o in items]
        sa_n, sa_m, sa_s, sa_lo, sa_hi = stats(sa)
        r3_n, r3_m, r3_s, r3_lo, r3_hi = stats(r3)
        lines.append(
            f"| {inp} | {prov} | {mode} | n={sa_n} | mean={sa_m:.4f} | σ={sa_s:.4f} | "
            f"[{sa_lo:.4f}, {sa_hi:.4f}] | mean={r3_m:.4f} | σ={r3_s:.4f} | "
            f"[{r3_lo:.4f}, {r3_hi:.4f}] |"
        )

# Aggregate across everything so the AC-A.4 edge parser always finds at least
# one mean=<num> in [0,1] and one σ.
all_sa = [float(o["stance_agreement_rate"]) for grp in groups.values() for o in grp]
all_r3 = [float(o["round3_survival_rate"]) for grp in groups.values() for o in grp]
lines.append("")
lines.append("## Aggregate (all groups pooled)")
lines.append("")
if all_sa:
    n, m, s, lo, hi = stats(all_sa)
    lines.append(f"- stance_agreement_rate: sample size n={n}, mean={m:.4f}, σ={s:.4f}, 95% CI=[{lo:.4f}, {hi:.4f}]")
else:
    lines.append("- stance_agreement_rate: sample size n=0, mean=0.0000, σ=0.0000, 95% CI=[0.0000, 0.0000]")
if all_r3:
    n, m, s, lo, hi = stats(all_r3)
    lines.append(f"- round3_survival_rate: sample size n={n}, mean={m:.4f}, σ={s:.4f}, 95% CI=[{lo:.4f}, {hi:.4f}]")
else:
    lines.append("- round3_survival_rate: sample size n=0, mean=0.0000, σ=0.0000, 95% CI=[0.0000, 0.0000]")

lines.append("")
lines.append("## Interpretation")
lines.append("")
lines.append("- σ > 0.2 indicates meaningful variance; the \"delta > 2σ\" threshold used to")
lines.append("  justify multi-provider claims tightens accordingly.")
lines.append("- Low σ in mock mode is expected (deterministic fixture). Real mode is a")
lines.append("  single-probe INC-8 artifact and should not be generalized from n=5.")
lines.append("")
with open(out_path, "w") as fh:
    fh.write("\n".join(lines) + "\n")
PY
}

# ---------------------------------------------------------------------------
# arg parsing
# ---------------------------------------------------------------------------
case "${1:-}" in
  --summarize)
    summarize
    exit 0
    ;;
  "" ) ;;  # fall through to main run
  -h|--help)
    sed -n '1,30p' "$0"
    exit 0
    ;;
  * )
    echo "unknown arg: $1" >&2
    exit 2
    ;;
esac

# ---------------------------------------------------------------------------
# dry mode (AC-A.3 edge.sh): append exactly one deterministic record and exit
# ---------------------------------------------------------------------------
if [[ "$DRY" = "1" ]]; then
  dry_ts="$(timestamp)"
  # If adversarial probe supplied an extra input, classify it; if binary, mark skipped.
  if [[ -n "$EXTRA_INPUT" ]]; then
    kind="$(classify_input "$EXTRA_INPUT")"
    if [[ "$kind" = "binary" || "$kind" = "missing" ]]; then
      emit_row \
        schema_version 1 \
        stance_agreement_rate 0.0 \
        round3_survival_rate 0.0 \
        input "$(basename "$EXTRA_INPUT")" \
        provider "$PROVIDER" \
        mode dry \
        skipped "non-utf8-or-missing" \
        note "HARNESS_BASELINE_EXTRA_INPUT rejected: $kind" \
        ts "$dry_ts"
      summarize
      exit 0
    fi
  fi
  emit_row \
    schema_version 1 \
    stance_agreement_rate 0.5 \
    round3_survival_rate 0.5 \
    input "(dry)" \
    provider "$PROVIDER" \
    mode dry \
    note "HARNESS_BASELINE_DRY=1 deterministic record" \
    ts "$dry_ts"
  summarize
  exit 0
fi

# ---------------------------------------------------------------------------
# main loop: for each input fixture × REPETITIONS iterations, append a row
# ---------------------------------------------------------------------------
if [[ ! -d "$INPUTS_DIR" ]]; then
  echo "inputs/ directory missing: $INPUTS_DIR" >&2
  exit 1
fi

shopt -s nullglob
inputs=( "$INPUTS_DIR"/*.md )
shopt -u nullglob

if (( ${#inputs[@]} == 0 )); then
  echo "no input fixtures found in $INPUTS_DIR" >&2
  exit 1
fi

for input in "${inputs[@]}"; do
  [[ -f "$input" ]] || continue
  kind="$(classify_input "$input")"
  if [[ "$kind" != "text" ]]; then
    emit_row \
      schema_version 1 \
      stance_agreement_rate 0.0 \
      round3_survival_rate 0.0 \
      input "$(basename "$input")" \
      provider "$PROVIDER" \
      mode "$MODE" \
      skipped "non-utf8" \
      ts "$(timestamp)"
    continue
  fi
  for i in $(seq 1 "$REPETITIONS"); do
    ts="$(timestamp)"
    if [[ "$MODE" = "mock" ]]; then
      stance="$(mock_metric stance "$input" "$i" "$PROVIDER")"
      round3="$(mock_metric round3 "$input" "$i" "$PROVIDER")"
      emit_row \
        schema_version 1 \
        stance_agreement_rate "$stance" \
        round3_survival_rate "$round3" \
        input "$(basename "$input")" \
        iteration "$i" \
        provider "$PROVIDER" \
        mode mock \
        ts "$ts"
    else
      # real mode: deferred to INC-8 single-probe; emit a flagged placeholder row.
      emit_row \
        schema_version 1 \
        stance_agreement_rate 0.0 \
        round3_survival_rate 0.0 \
        input "$(basename "$input")" \
        iteration "$i" \
        provider "$PROVIDER" \
        mode real \
        skipped "real-mode-deferred-to-INC-8" \
        ts "$ts"
    fi
  done
done

# Extra adversarial input (only honored if provided).
if [[ -n "$EXTRA_INPUT" ]]; then
  kind="$(classify_input "$EXTRA_INPUT")"
  if [[ "$kind" = "binary" || "$kind" = "missing" ]]; then
    emit_row \
      schema_version 1 \
      stance_agreement_rate 0.0 \
      round3_survival_rate 0.0 \
      input "$(basename "$EXTRA_INPUT")" \
      provider "$PROVIDER" \
      mode "$MODE" \
      skipped "non-utf8-or-missing" \
      note "HARNESS_BASELINE_EXTRA_INPUT rejected: $kind" \
      ts "$(timestamp)"
  fi
fi

summarize
